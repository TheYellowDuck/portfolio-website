// Emits src/data/skill-categories.generated.ts — a { skill/tech name → category } map derived from
// the static taxonomy in github-sync.config.mjs (SKILL_CATEGORY + EXTRA_SKILL_CATEGORIES). This lets
// the site colour ANY chip by its category, including tech that never appears in a scanned repo (e.g.
// Experience-only tools). It reads ONLY the config — no network, no repo scan — so run it directly:
//   node scripts/gen-skill-categories.mjs
// Re-run whenever the taxonomy in the config changes. (The Concepts list is skipped: those are
// category names themselves, already grouped per-project by the sync.)
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { SKILL_CATEGORY, EXTRA_SKILL_CATEGORIES } from "./github-sync.config.mjs";

const map = {};
const claim = (name, category) => { if (name && category && !(name in map)) map[name] = category; };

// Objective tech taxonomy → its category (skip "Concepts": those entries ARE category names).
for (const [category, names] of Object.entries(SKILL_CATEGORY)) {
  if (category === "Concepts") continue;
  for (const name of names) claim(name, category);
}
// Extra, colour-only taxonomy (e.g. Experience-only tech).
for (const [category, names] of Object.entries(EXTRA_SKILL_CATEGORIES || {})) {
  for (const name of names) claim(name, category);
}

const sorted = Object.fromEntries(Object.entries(map).sort(([a], [b]) => a.localeCompare(b)));
const __dirname = path.dirname(fileURLToPath(import.meta.url));
const out = path.join(__dirname, "..", "src", "data", "skill-categories.generated.ts");
const body =
  "// ⚠ AUTO-GENERATED by scripts/gen-skill-categories.mjs — do not edit by hand.\n" +
  "// Run `node scripts/gen-skill-categories.mjs` after changing the taxonomy in github-sync.config.mjs.\n" +
  "// Maps a skill/tech name → its category, so every chip can be coloured by category site-wide.\n\n" +
  `export const generatedSkillCategories: Record<string, string> = ${JSON.stringify(sorted, null, 2)};\n`;
fs.writeFileSync(out, body);
console.log(`Wrote ${Object.keys(sorted).length} skill→category entries to ${out}`);
